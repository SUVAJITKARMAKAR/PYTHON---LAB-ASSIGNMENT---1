# A PROGRAM TO DETERMINE A SET USING DIFFERENT DATATYPES AND PERFORM ASSIGNED FUNCTIONS 

domain_set = { "SUVAJITKARMAKAR", "SOCIALNETWORKINGWEBSITE", 2023, 2347261, True}

#discarding a specific element from the set using discard() method.
domain_set.discard("SOCIALNETWORKINGWEBSITE")
print(domain_set)

# popping a specific element from the set using pop() method.
domain_set.pop()
print(domain_set)
# clearing the set using the clear() method.
domain_set.clear()
print(domain_set)

# deleting the whole set using del
del domain_set
domain_set
print(domain_set)